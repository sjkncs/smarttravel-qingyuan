// 合并基础知识库 + 扩展知识库
const allDocuments: RAGDocument[] = [
  ...knowledgeBase.map((doc) => ({ ...doc, source: "builtin" as const })),
  ...expandedKnowledge,
];

// 单例: BM25 + TF-IDF 混合检索器 (应用启动时构建索引)
let _retriever: HybridRetriever | null = null;
function getRetriever(): HybridRetriever {
  if (!_retriever) {
    _retriever = new HybridRetriever(allDocuments);
    const stats = _retriever.getStats();
    console.log(
      `[RAG] BM25+TF-IDF index built: ${stats.docCount} docs, ${stats.vocabSize} terms`
    );
  }
  return _retriever;
}

/**
 * ML增强文档检索 — BM25 + TF-IDF 混合评分
 * 替代旧的简易TF-IDF检索，检索质量大幅提升
 */
export function retrieveDocuments(query: string, topK: number = 3): KBDocument[] {
  const retriever = getRetriever();
  const results = retriever.searchSync(query, topK);
  if (results.length === 0) return knowledgeBase.slice(0, topK);
  return results.map((r) => ({
    id: r.doc.id,
    title: r.doc.title,
    category: r.doc.category as KBDocument["category"],
    tags: r.doc.tags,
    content: r.doc.content,
  }));
}

/**
 * 异步混合检索 (含可选语义嵌入)
 */
export async function retrieveDocumentsAsync(
  query: string,
  topK: number = 5,
  useEmbedding: boolean = false
) {
  const retriever = getRetriever();
  return retriever.search(query, topK, useEmbedding);
}

/**
 * 获取检索引擎统计信息
 */
export function getRAGStats() {
  const retriever = getRetriever();
  return retriever.getStats();
}

